                        <h2>Ethical, Practical, Beautiful &amp; Unusual</h2>
                        <p>Emma's Emporium sources alternative and unique fashion, accessories and home interiors; our buying policy is Ethical, Practical, Beautiful and Unusual.</p>
                        <p>Working to Fair Trade principles, we stock a range of clothes and accessories to suit a variety of tastes. Emma's Emporium sources classic Indian cotton prints and silks, vibrant colours and alternative styles, such as our large selection of Harem trousers and silky tops and skirts. We have a winter range of quality Alpaca knitwear, Pure wool hand knit accessories and handmade home wares.</p>
                        <p>Emma's Emporium <a title="Wholesale" href="http://www.emmasemporium.org/wholesale">supply wholesale</a> goods to small independent retailers across the South West and East Anglia. We sell to the public through direct retail at festivals and events. A limited selection (mainly our popular Harem &amp; Genie trousers) are available to buy in<a href="http://www.ebay.co.uk/usr/emmas_emporium11"> our eBay shop – Emma's Emporium Textiles</a>. Click the <a title="Catalogue" href="http://www.emmasemporium.org/catalogue">catalogue</a> images to go to our eBay shop or visit our regularly updated <a href="https://www.facebook.com/pages/Emmas-emporium/254362121267512">facebook page</a>.</p>
                        <p>As our business grows, so does that of our trading partners; as we grow we head further around the globe to find new treasures and increase the diversity of our collection.</p>
                        <p> At Emma's Emporium we believe Fair Trade is the only way to trade; we buy direct from the manufacturer to ensure they get a fair price and so do you. Our stock sourcing principles:</p>
                        <ul>
                            <li>No child labour is involved in the manufacture of any of our products .</li>
                            <li>All work is carried out in clean and safe working conditions .</li>
                            <li>We pay a fair price for our products to ensure fair wages for our producers.</li>
                            <li>We pay in advance to ensure wages are paid on time and no debt is incurred when sourcing material.</li>
                        </ul>
